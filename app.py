@contextlib.asynccontextmanager
async def lifespan(app: FastAPI):
    logger.info("Bootstrapping database…")
    db.bootstrap_schema()
    logger.info("Starting background scheduler…")
    sched.start_scheduler()
    yield
    sched.stop_scheduler()


