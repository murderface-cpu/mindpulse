@app.get("/", response_class=HTMLResponse)
async def home(request: Request):
    stats = db.fetch_stats()
    featured = [parse_insight(i) for i in db.fetch_insights(limit=1)]
    insights = [parse_insight(i) for i in db.fetch_insights(limit=10, offset=1)]
    next_run = sched.get_next_run()
    interval = int(os.getenv("POPULATE_INTERVAL_SECONDS", "300"))

    return templates.TemplateResponse("index.html", {
        "request": request,
        "stats": stats,
        "featured": featured,
        "insights": insights,
        "next_run": next_run,
        "interval": interval,
    })


@app.get("/insight/{insight_id}", response_class=HTMLResponse)
async def insight_detail(request: Request, insight_id: int):
    """Individual article page"""
    item = db.fetch_insight_detail(insight_id)
    if not item:
        raise HTTPException(status_code=404, detail="Article not found")
    
    if item.get('full_content'):
        raw = item['full_content']

        # Extract summary
        summary_match = re.search(r'"summary"\s*:\s*"(.*?)"(?=\s*,\s*")', raw, re.DOTALL)
        item['summary'] = summary_match.group(1).strip() if summary_match else item.get('summary', '')

        # Extract full_content (the long article text)
        content_match = re.search(r'"full_content"\s*:\s*"(.*?)"(?=\s*,\s*"\w|}\s*$)', raw, re.DOTALL)
        item['full_content'] = content_match.group(1).strip() if content_match else raw

        # Extract key_points as a list
        key_points_block = re.search(r'"key_points"\s*:\s*\[(.*?)\]', raw, re.DOTALL)
        if key_points_block:
            item['key_points'] = re.findall(r'"(.*?)"(?=\s*[,\]])', key_points_block.group(1), re.DOTALL)
        else:
            item['key_points'] = []

    return templates.TemplateResponse("detail.html", {
        "request": request,
        "item": item,
    })


