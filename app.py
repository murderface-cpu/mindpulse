def parse_insight(item: dict) -> dict:
    # Use summary field as raw since that's where the JSON is stored
    raw = item.get('full_content', '')
    if not raw:
        return item

    summary_match = re.search(r'"summary"\s*:\s*"(.*?)"(?=\s*,\s*\n\s*")', raw, re.DOTALL)
    item['summary'] = summary_match.group(1).strip() if summary_match else raw

    content_match = re.search(r'"full_content"\s*:\s*"(.*?)"(?=\s*,\s*\n\s*"|\s*\n\s*})', raw, re.DOTALL)
    item['full_content'] = content_match.group(1).strip() if content_match else raw

    key_points_block = re.search(r'"key_points"\s*:\s*\[(.*?)\]', raw, re.DOTALL)
    if key_points_block:
        item['key_points'] = re.findall(r'"(.*?)"(?=\s*[,\]])', key_points_block.group(1), re.DOTALL)
    else:
        item['key_points'] = []

    return item

