# ── REST API ───────────────────────────────────────────────────────────────────

@app.get("/api/insights")
async def api_insights(
    limit: int = Query(default=20, ge=1, le=100),
    offset: int = Query(default=0, ge=0),
    category: str | None = Query(default=None),
):
    rows = db.fetch_insights(limit=limit, offset=offset, category=category)
    # Convert datetime objects to strings
    for r in rows:
        if r.get("created_at"):
            r["created_at"] = r["created_at"].isoformat()
    return {"data": rows, "count": len(rows)}


@app.get("/api/stats")
async def api_stats():
    stats = db.fetch_stats()
    # Serialise datetimes
    for run in stats.get("recent_runs", []):
        if run.get("run_at"):
            run["run_at"] = run["run_at"].isoformat()
    stats["next_run"] = sched.get_next_run()
    stats["interval_seconds"] = int(os.getenv("POPULATE_INTERVAL_SECONDS", "300"))
    return stats


@app.get("/api/logs")
async def api_logs(limit: int = Query(default=50, ge=1, le=500)):
    logs = db.fetch_logs(limit=limit)
    for log in logs:
        if log.get("created_at"):
            log["created_at"] = log["created_at"].isoformat()
    return {"data": logs}


class IntervalRequest(BaseModel):
    seconds: int


@app.post("/api/scheduler/interval")
async def set_interval(body: IntervalRequest):
    if body.seconds < 30:
        raise HTTPException(status_code=400, detail="Interval must be ≥ 30 seconds")
    sched.update_interval(body.seconds)
    return {"message": f"Interval updated to {body.seconds}s"}


@app.post("/api/scheduler/run-now")
async def run_now():
    """Trigger an immediate population run (async via scheduler)."""
    import threading
    t = threading.Thread(target=sched.populate_database, daemon=True)
    t.start()
    return {"message": "Population run triggered"}


